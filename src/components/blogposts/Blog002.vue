            I can remember a time when I was building a site using this popular framework. This framework was so popular, I felt that anything that I could ever ask would have already been asked, and answered by someone else. Alas, my project errored out at runtime with error codes and references that led so deep within the framework’s code that I was bewildered. I grew frustrated debugging, searching error codes, and message boards trying to figure it out. It turns out that the framework’s development team were in between releases, and the bugs were resolved within about 48 hours. To this day, I can’t tell you what in the framework was broken, nor what was fixed. I did however, learn a lot of other important things during those 48 hours!
          </p>

