            Sleep. Get a nice bed and sleep. Sleep so that when you’re awake, you’re awake. Don’t half sleep, and then be half awake, you’ll get half done. Here’s a cool thing. Some days you’ll go to bed with some unsolved coding problem. You’ll have a dream, and wake up with a solution. If you follow Tip 2, and saturate yourself with info, your subconscious mind will code for you while you sleep! Boss!
            Breathe. Look away from the monitor, get up and step outside into the sun (or moonlight), and breathe deeply from your diaphragm. Get your heartrate up, jog, chase a squirrel, do jumping jacks, look at your student loan statement… too far? The point is, sitting down for long periods hunched over a screen is very very bad, you must downward dog, or cat stretch or something to break it up.
