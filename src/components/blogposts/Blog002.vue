            One fateful night after working late and then burning the midnight oil on some project. I nodded off. I was so tired that I headed straight for bed without putting up my hardware. I have small children so I ALWAYS put my things up and away from their sticky paws when not in use; but this night, I left my laptop out on the coffee table.
            The next morning I opened my laptop eager to pick up where I left off, only to find that the trackpad and keyboard input was so wacky that I couldn’t even log in. It turns out that while I was asleep, my daughter in an attempt to play Minecraft, destroyed my computer with blunt force, and water… the details are still mysterious, but the point is I lost a lot of work. If I had been practicing proper version control via one of the Git clients, or at least had regular back ups, the loss wouldn’t have been so catastrophic. I hate to sound redundant but, redundancy, redundancy, redundancy!
          </p>

          <h3 class="text-white">
            Tip 8
            <span class="paragraph-title">
              <b>Music please!</b>
            </span>
          </h3>

          <p class="text-white">
            I may lose some of you here because music is highly subjective, but I can share with you what works for me. If you followed my instructions from Tip 4b, then by now you possess a glorious pair of over-the-ear headphones. Personally, I have two specific listening modes, depending on what I’m trying to achieve. For instance, let’s say I’m brainstorming the concept for a new video game. The plot, the characters, the art etc. For me, classical music is best. Most classical has a narrative structure that resonates well for conceptualizing and storytelling.
            On the other hand, if I am writing code, I find the sounds that focus my attention best are any of the “lo-fi hip hop” stations on youtube or mobile apps. You’re probably thinking hip-hop? really? The thing about the lo-fi hip hop is it usually has less that 2% lyric content, but mostly very relaxing, and persistent grooves that more or less fade into ambience, so much so that nothing fills your head except the code before you. Unless the beat is particularly sick, of course. At least, that’s how I experience it. You may focus best coding to Deathcore… no judgement.
          </p>

